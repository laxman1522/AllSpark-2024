var allSparkModule = angular
  .module('allSparkModule', [])

  .controller('allSparkController', function ($scope, $http) {
    $scope.timelines = FLASHBACK_EVENTS;
    $scope.sessions = ALLSPARK_SESSIONS;
    $scope.solSpace = SOL_SPACE_EVENTS;
    $scope.day1 = true;

    $scope.toggleContent = function (e) {
      Array.from(e.target.parentElement.children).map(
        (elem) => (elem.className = ''),
      );
      e.target.className = 'active';
      if ($scope.day1) {
        $scope.day1 = false;
      } else {
        $scope.day1 = true;
      }
    };
  });
